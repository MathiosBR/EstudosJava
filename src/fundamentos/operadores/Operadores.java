package fundamentos.operadores;

public class Operadores {
	public static void main(String[] args) {
		//Operadores Unários
		int a = 1;
		a++; // Apenas um operando e pós-fixada
		System.out.println("Novo valor de 'a': " + a);
		--a; // Unário e pré-fixada
		System.out.println("Novo valor de 'a': " + a);
		//Operadores Binários
		int b = 3 + 2; // Dois operandos e infixo (o '+' está entre operandos)
		System.out.println(b);
		
		//Operador Térnario
		// v ? v : u
		
	}
}
